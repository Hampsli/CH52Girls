/**
 * Clase que representa los nombres de tres amigos.
 */
export class Friends {
  /**
   * Crea una instancia de Friends.
   * @param {string} name1 - Nombre del primer amigo.
   * @param {string} name2 - Nombre del segundo amigo.
   * @param {string} name3 - Nombre del tercer amigo.
   */
  constructor(name1, name2, name3) {
    this.name1 = name1;
    this.name2 = name2;
    this.name3 = name3;
  }
}